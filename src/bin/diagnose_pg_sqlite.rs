#![allow(
    clippy::doc_markdown,
    clippy::print_literal,
    clippy::too_many_lines,
    clippy::items_after_statements
)]

//! Produces `dataset_failures.md`: a detailed breakdown of every statement in
//! the `postgresql` and `sqlite` dataset groups that at least one parser cannot
//! handle, using the appropriate dialect for each group:
//!
//!   postgresql datasets -> PostgreSqlDialect
//!   sqlite     datasets -> SQLiteDialect
//!
//! For each dataset the document shows:
//!   1. Summary counts
//!   2. Statements accepted by pg_query but rejected by sqlparser (parser gap)
//!   3. Statements rejected by pg_query (invalid / non-PG SQL), split into
//!      those sqlparser does and does not accept.
//!
//! Requires the default `pg_query_parser` feature.
//!   cargo run --bin diagnose_pg_sqlite

#[cfg(feature = "pg_query_parser")]
use sql_ast_benchmark::is_valid_pg_query;

use sql_ast_benchmark::is_valid_sqlparser;

#[cfg(not(feature = "pg_query_parser"))]
fn main() {
    eprintln!("ERROR: pg_query_parser feature is required.");
    eprintln!("Run: cargo run --bin diagnose_pg_sqlite");
    std::process::exit(1);
}

#[cfg(feature = "pg_query_parser")]
fn main() {
    use sqlparser::dialect::SQLiteDialect;
    use sqlparser::parser::Parser;
    use std::fmt::Write as FmtWrite;
    use std::fs;

    // silence panics from parsers that use panic! for error paths
    std::panic::set_hook(Box::new(|_| {}));

    // ── Dataset groups ────────────────────────────────────────────────────────
    // (file path, dialect label for report)
    struct Dataset {
        path: &'static str,
        dialect: &'static str, // "postgresql" or "sqlite"
    }

    let datasets: &[Dataset] = &[
        Dataset {
            path: "datasets/postgresql/bird_pg.txt",
            dialect: "postgresql",
        },
        Dataset {
            path: "datasets/postgresql/defog_data.txt",
            dialect: "postgresql",
        },
        Dataset {
            path: "datasets/postgresql/defog_sql.txt",
            dialect: "postgresql",
        },
        Dataset {
            path: "datasets/postgresql/pg_regress.txt",
            dialect: "postgresql",
        },
        Dataset {
            path: "datasets/sqlite/sede.txt",
            dialect: "sqlite",
        },
        Dataset {
            path: "datasets/sqlite/spider_sqlite.txt",
            dialect: "sqlite",
        },
        Dataset {
            path: "datasets/sqlite/sql_create_ctx.txt",
            dialect: "sqlite",
        },
    ];

    let mut doc = String::new();

    writeln!(doc, "# Dataset Failure Analysis — PostgreSQL and SQLite").unwrap();
    writeln!(doc).unwrap();
    writeln!(doc, "Generated by `cargo run --bin diagnose_pg_sqlite`.").unwrap();
    writeln!(doc).unwrap();
    writeln!(doc, "Dialects used:").unwrap();
    writeln!(doc, "- **postgresql** datasets → `PostgreSqlDialect`").unwrap();
    writeln!(doc, "- **sqlite** datasets → `SQLiteDialect`").unwrap();
    writeln!(doc).unwrap();
    writeln!(
        doc,
        "**pg_query** = libpg_query (the actual PostgreSQL parser, ground truth for PG validity)"
    )
    .unwrap();
    writeln!(doc).unwrap();
    writeln!(doc, "---").unwrap();
    writeln!(doc).unwrap();

    for ds in datasets {
        let path = std::path::Path::new(ds.path);
        if !path.exists() {
            writeln!(doc, "## {}", ds.path).unwrap();
            writeln!(doc).unwrap();
            writeln!(doc, "> File not found — skipped.").unwrap();
            writeln!(doc).unwrap();
            continue;
        }

        let content = fs::read_to_string(path).expect("read error");
        let stmts: Vec<&str> = content.lines().filter(|l| !l.trim().is_empty()).collect();
        let total = stmts.len();

        // Classify each statement
        let mut pg_pass_sql_pass: Vec<&str> = Vec::new();
        let mut pg_pass_sql_fail: Vec<&str> = Vec::new();
        let mut pg_fail_sql_pass: Vec<&str> = Vec::new();
        let mut pg_fail_sql_fail: Vec<&str> = Vec::new();

        for stmt in &stmts {
            let pg_ok = is_valid_pg_query(stmt);
            let sql_ok = match ds.dialect {
                "postgresql" => is_valid_sqlparser(stmt),
                "sqlite" => Parser::parse_sql(&SQLiteDialect {}, stmt).is_ok(),
                _ => unreachable!(),
            };
            match (pg_ok, sql_ok) {
                (true, true) => pg_pass_sql_pass.push(stmt),
                (true, false) => pg_pass_sql_fail.push(stmt),
                (false, true) => pg_fail_sql_pass.push(stmt),
                (false, false) => pg_fail_sql_fail.push(stmt),
            }
        }

        let pg_fail_total = pg_fail_sql_pass.len() + pg_fail_sql_fail.len();

        // ── Section header ──────────────────────────────────────────────────
        writeln!(doc, "## `{}` (dialect: {})", ds.path, ds.dialect).unwrap();
        writeln!(doc).unwrap();
        writeln!(doc, "| Category | Count | % of total |").unwrap();
        writeln!(doc, "|---|---|---|").unwrap();
        writeln!(doc, "| Total statements | {total} | 100% |").unwrap();
        writeln!(
            doc,
            "| pg_query ✓  sqlparser ✓ | {} | {:.1}% |",
            pg_pass_sql_pass.len(),
            pct(pg_pass_sql_pass.len(), total)
        )
        .unwrap();
        writeln!(
            doc,
            "| pg_query ✓  sqlparser ✗ | {} | {:.1}% |",
            pg_pass_sql_fail.len(),
            pct(pg_pass_sql_fail.len(), total)
        )
        .unwrap();
        writeln!(
            doc,
            "| pg_query ✗  sqlparser ✓ | {} | {:.1}% |",
            pg_fail_sql_pass.len(),
            pct(pg_fail_sql_pass.len(), total)
        )
        .unwrap();
        writeln!(
            doc,
            "| pg_query ✗  sqlparser ✗ | {} | {:.1}% |",
            pg_fail_sql_fail.len(),
            pct(pg_fail_sql_fail.len(), total)
        )
        .unwrap();
        writeln!(doc).unwrap();

        // ── pg_query ✓, sqlparser ✗ — parser gaps ───────────────────────────
        if !pg_pass_sql_fail.is_empty() {
            writeln!(
                doc,
                "### pg_query ✓ / sqlparser ✗  —  parser gaps ({} statements)",
                pg_pass_sql_fail.len()
            )
            .unwrap();
            writeln!(doc).unwrap();
            writeln!(
                doc,
                "> These are valid PostgreSQL statements that sqlparser-rs fails to parse."
            )
            .unwrap();
            writeln!(doc).unwrap();
            for stmt in &pg_pass_sql_fail {
                writeln!(doc, "```sql").unwrap();
                writeln!(doc, "{stmt}").unwrap();
                writeln!(doc, "```").unwrap();
                writeln!(doc).unwrap();
            }
        }

        // ── pg_query ✗ ────────────────────────────────────────────────────────
        if pg_fail_total > 0 {
            writeln!(
                doc,
                "### pg_query ✗  —  not valid PostgreSQL ({pg_fail_total} statements)"
            )
            .unwrap();
            writeln!(doc).unwrap();
            writeln!(doc, "> pg_query rejects these. They may be non-PostgreSQL dialect, malformed, or placeholder/template SQL.").unwrap();
            writeln!(doc).unwrap();

            if !pg_fail_sql_pass.is_empty() {
                writeln!(
                    doc,
                    "#### sqlparser accepts these ({} statements)",
                    pg_fail_sql_pass.len()
                )
                .unwrap();
                writeln!(doc).unwrap();
                for stmt in &pg_fail_sql_pass {
                    writeln!(doc, "```sql").unwrap();
                    writeln!(doc, "{stmt}").unwrap();
                    writeln!(doc, "```").unwrap();
                    writeln!(doc).unwrap();
                }
            }

            if !pg_fail_sql_fail.is_empty() {
                writeln!(
                    doc,
                    "#### both parsers reject these ({} statements)",
                    pg_fail_sql_fail.len()
                )
                .unwrap();
                writeln!(doc).unwrap();
                for stmt in &pg_fail_sql_fail {
                    writeln!(doc, "```sql").unwrap();
                    writeln!(doc, "{stmt}").unwrap();
                    writeln!(doc, "```").unwrap();
                    writeln!(doc).unwrap();
                }
            }
        }

        writeln!(doc, "---").unwrap();
        writeln!(doc).unwrap();

        // Progress
        eprintln!(
            "  done: {} ({} stmts, {} sql-gaps, {} pg-fail)",
            ds.path,
            total,
            pg_pass_sql_fail.len(),
            pg_fail_total
        );
    }

    let out_path = "dataset_failures.md";
    fs::write(out_path, &doc).expect("failed to write output file");
    println!("Written to {out_path}  ({} bytes)", doc.len());
}

fn pct(n: usize, base: usize) -> f64 {
    if base == 0 {
        0.0
    } else {
        100.0 * n as f64 / base as f64
    }
}
